# Hỏi người chơi “Ban hay dien vao mot con so“.
# So sánh số đó với 100, nếu số đó lớn hơn 100 thì in ra “Cao hon 100”,
# nếu không thì in ra “Thap hon hoac bang 100"

number = int(input('Ban hay dien vao mot con so '))

if number > 100:
    print('Cao hon 100')

else:
    print('Thap hon hoac bang 100')
