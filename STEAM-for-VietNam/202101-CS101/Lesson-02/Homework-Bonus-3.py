# Hint: 1 hàm không cần câu lệnh return cũng hợp lệ.

def sing_happy_birthday(ten):
    print('Happy Birthday to You')
    print('Happy Birthday to You')
    print('Happy Birthday Dear ' + ten)
    print('Happy Birthday to You.')


