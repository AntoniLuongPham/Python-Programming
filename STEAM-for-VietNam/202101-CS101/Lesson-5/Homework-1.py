# Bài 1: Push

# Các con đã học về Stack trong lớp, chúng ta hãy cùng viết các hàm cho nó nhé!
# Chúng ta hãy bắt đầu bằng hàm push

# Yêu cầu:
# - Viết hàm push cho stack
# - Hàm sẽ thêm phần tử vào vị trí cuối cùng của một mảng



stack = [2, 6, 9]

# ví dụ 1:
push(stack, 1)
print(stack)
# [2, 6, 9, 1]

# ví dụ 2:
push(stack, 4)
print(stack)
# [2, 6, 9, 1, 4]

# ví dụ 3:
push(stack, 3)
print(stack)
# [2, 6, 9, 1, 4, 3]
