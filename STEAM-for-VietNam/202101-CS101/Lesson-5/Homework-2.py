# Bài 2: Pop

# Sau khi viết hàm push, bây giờ chúng ta sẽ viết hàm pop cho stack.

# Yêu cầu:
# - Viết hàm pop cho stack
# - Hàm sẽ xóa phần tử ở vị trí cuối cùng của một mảng
# và trả về phần tử vừa xóa đó



stack = [2, 6, 9, 1, 4, 3]

# ví dụ 1: 
print(pop(stack))
print(stack)
# 3
# [2, 6, 9, 1, 4]

# ví dụ 2:
print(pop(stack))
print(stack)
# 4
# [2, 6, 9, 1]

# ví dụ 3:
print(pop(stack))
print(stack)
# 1
# [2, 6, 9]
